	char optext[10000];
	char key[10];
	char citext[10000];
	char trans[10000];
	char A[10];
	char B[10];
	cout<< "Enter key in bits, but not more than 40 bits\n";
	while (s<40)
	{
	cin>>ch;
	if ((ch=='0')||(ch=='1'))
	{
		buf[s]=ch;
		s++;
	}
	else
	{
	cout<<"Enter '0' or '1'\n";
	s=s;
	}
	}
	s=0;
	while (m<10)
	{
		key[m]=binTOhex(buf[s],buf[s+1],buf[s+2],buf[s+3]);
		m++;
		s=s+4;
	}
	ifstream in("Opentext.txt");
	ofstream out("Cipher.enc");
