	if ((i-(i/5)*5)!=0)
	{
		j=5*(i/5+1);
	}
	else
	{
	j=i;
	}
	in.close();
	if ((j-(j/2)*2)!=0)
	{
	j=j+5;
	}
	while (i<j)
	{
	buf[i]='0';
	i++;
	}
	p=2*j;
	s=0;
	m=0;
	while (s<j)
	{
	optext[m]=asciiTOhex1(buf[s]);
	optext[m+1]=asciiTOhex2(buf[s]);
	m=m+2;
	s++;
	}
	s=0;
